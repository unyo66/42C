int	ft_strcmp(char	*s1, char	*s2)
{
	int	i;
	int	val;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		val = s1[i] - s2[i];
		if (val == 0)
			i++;
		else
			return (val);
	}
	return (0);
}
